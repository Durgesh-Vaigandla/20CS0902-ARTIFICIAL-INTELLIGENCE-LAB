# Nested List
nested_list = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# Length of a List
length_of_list = len(nested_list)

# Concatenation of Lists
list1 = [1, 2, 3]
list2 = [4, 5, 6]
concatenated_list = list1 + list2

# Membership Test
element_to_check = 3
is_member = element_to_check in list1

# Iteration through List
print("Elements in list1:")
for element in list1:
    print(element)

# Indexing
first_element = list1[0]

# Slicing
sliced_list = list1[1:3]

# Displaying Results
print("Nested List:", nested_list)
print("Length of List:", length_of_list)
print("Concatenated List:", concatenated_list)
print(f"Is {element_to_check} a member of list1? {is_member}")
print("First Element of list1:", first_element)
print("Sliced List:", sliced_list)
